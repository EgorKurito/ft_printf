#include "ft_printf.h"

void	ft_putnbr(ssize_t n)
{
	if (n / 10 != 0)
	{
		if (n < 0)
			ft_putnbr(-(n / 10));
		else
			ft_putnbr(n / 10);
	}
	if (n < 0)
		ft_putchar_n(-(n % 10) + '0', 1);
	else
		ft_putchar_n(n % 10 + '0', 1);
}
